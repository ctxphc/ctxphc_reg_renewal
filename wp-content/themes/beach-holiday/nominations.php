		<h2>TAMMY CAMP</h2>
	</div>

	<div class=spacer></div>


	<p>I'm excited about the prospect of serving as the Vice President of the Central Texas Parrothead Club.  I've been a member of this club for almost 12 years and take great pride in our organization.
	<p>I would like to make the role of Vice President an active position, not only a supporting role to the President, by adding a focus on special projects for the club as well as an active "helping hand" to each of my fellow Board members. For special projects, I would like to focus on member appreciation programs to help new members become actively involved in our club, as well as increase the level of engagement from current members. My ideas include:
	<ul>
		<li><strong>Member Appreciation Program:</strong>A rewards and recognition program to spotlight the efforts of general members.  Many of our members put in a lot of effort in our club and should be recognized.  I have several ideas for this program and look forward to finding out what your ideas are to ensure the rewards and recognition are things you will enjoy.</strong></li>
		<li><strong>New Member Ambassador Program:</strong>I would work closely with the Membership Director to make this a great success.  I've been part of other clubs where each new member is assigned to an "Ambassador"(a seasoned club member) who's role, among other things, would be to take an new member under their wing and integrate them into the phlock.  The Ambassador would send personal emails encouraging the new members to attend events and would act as host to the new member at two Happy Hours introducing them to other members.  This would help new members get to know everyone and ensure they feel welcome and a part of the group.</strong></li>
	</ul>
	<p>Along with these two new programs, I feel strongly that the Vice President position should actively help other officers with their projects.  I will offer my assistance to the other Board Members and committees to help with what ever they need!
	<p>Thank you for considering me for the Vice President role.  I would love to serve our club again next year.
