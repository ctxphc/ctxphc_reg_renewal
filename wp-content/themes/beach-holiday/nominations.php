	<div class="post_title">
		<h2>DOUG HALL</h2>
	</div>
	<div class=spacer></div>


	<p>Quite simply, I am running for my second term as President because I want to finish what I started.  To take this club back to the prominence it once enjoyed.  A club that other clubs would want to model themselves after.  To bring all the members of this club closer together.  And to increase on a continual basis our presence in the community and our commitment to our causes. 
	<p>We made some very good strides this year despite some early setbacks.  And it is those strides I want to expand on and move forward with.  There is still a lot of work to be done.  And make no mistake, it will take someone with almost ten years Board experience in the roles of Philanthropy, Vice President, and President.  Someone known throughout the South Region as well as within this club as someone who shoots straight and does not sugar coat, someone who does his best to represent the club as whole while keeping the back of each individual member in the process, and someone known to stay open minded to any ideas or suggestions while at the same time standing his ground on issues he feels the strongest about.  That of course means that I will never be the most popular person to everyone at one time.  But I promise that I will be popular to everyone of you at some time or another.  It is part of the job and you need someone who understands that and will move forward using his best judgment. 
	<p>I like to think I have done well for this club during my time on the Board. Moving into the Presidency was not an easy transition.  It is a lot of hard work and it came at a difficult time in our club's history.  A lot of folks told me I was crazy for taking it on.  A lot of others have told me I am even crazier for wanting to do it again.  Maybe, maybe not.  But it answers the question of my commitment to this club and to every single one of you.  This is a passion.  
	<p>I have seen a lot of progress.  Some of it in some unlikely places that others may have missed. And I want to keep that going.
	<p>Thank you for your consideration
</div>

<div>
	<div class="post_title">
		<h2>TONY KYLES</h2>
	</div>
	
	<div class=spacer></div>

	<p>In accepting the nomination for the president of Central Texas Parrothead Club, I am greatly excited to become a more integral part of this PHlock and learn more from all the great people who have come before me and to those who may come after me. 
	<p>In joining any organization whether it be on a professional or personal level I have always understood that one must realize it is not up to one individual but many that will help that organization to achieve its goals and visions.  I hope to be able to bring a variety of life experiences to the presidency in making our club one of the most successful throughout the great Parrot Head Nation. 
	<p>From a young age I became involved in and saw the benefits of community service, working during my teenage years helping out local food pantries, Toy’s for Tots, and other worthy causes that gave me real world exposure to community outreach.  I continued this pattern through my undergraduate studies at the University of Wisconsin-Madison and even more so when I became the president of the School of Business’s Marketing Society Mu Kappa Tau.  Starting off as a member, being elected as the Social Chair and eventually being promoted to President allowed me to understand the different roles and responsibilities that go into making an non-profit organization successful, from fund-raising to marketing/membership, and again community philanthropic works. 
	<p>After graduating from school and working in a variety of industries over the last 10 years such as health care, insurance and financial services, and even security, I have been blessed to have been exposed to so many wonderful people from different backgrounds which have helped me to gain a better perspective of not only who I am but what I can accomplish by myself as well as with the help of others. 
	<p>If elected to serve on the 2013 Board of Directors for the CTXPHC, I will make it my goal to keep the lessons and people that have created this wonderful club in my mind as we move forward in expanding the great successes that the organization has achieved.  With that being said, there are always opportunities for future new endeavors that will expand the club’s abilities to serve even more members and community partners and am excited to be a part of initiating those endeavors. 
	<p>I look forward to answering any questions and/or concerns about my candidacy and have provided both my email and phone below knowing that in order to excel in a leadership role one must be open to all viewpoints and perspectives in obtaining the necessary information to make the most informed and best suited decisions for a group cause.

	<p>I thank you for the opportunity and promise that I will do everything in my power to exceed the expectations that this Presidency and organization calls for.
</div>

